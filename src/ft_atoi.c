#include "../libft.h"

int	ft_atoi(const char *str)
{
	int	i[2];

	i[0] = 0;
	i[1] = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			i[1] = -1;
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		i[0] = i[0] * 10;
		i[0] = i[0] + (*str - 48);
		str++;
	}
	return (i[1] * i[0]);
}
